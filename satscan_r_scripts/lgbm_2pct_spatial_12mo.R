# 4. Run SaTScan --------------------------------------------------------------
saTScanPath <- "C:/Users/kanderson4/Desktop"

start_time <- Sys.time()

satscan_results <- satscan(output_dir,
                           "lgbm_2pct_spatial_12mo",
                           sslocation      = saTScanPath,
                           ssbatchfilename = "SaTScanBatch64",
                           verbose         = TRUE)

end_time <- Sys.time()
print(round(end_time - start_time, 2))

# 5. Quick visual check of Monte-Carlo LLRs -----------------------------------
if (!is.null(satscan_results$llr)) {
  llr_values <- unlist(satscan_results$llr)
  if (is.numeric(llr_values) && length(llr_values) > 0) {
    hist(llr_values,
         main = "Monte Carlo Log-Likelihood Ratios",
         xlab = "Log-Likelihood Ratio",
         ylab = "Frequency")
  }
}

# 6. Inspect results ----------------------------------------------------------
